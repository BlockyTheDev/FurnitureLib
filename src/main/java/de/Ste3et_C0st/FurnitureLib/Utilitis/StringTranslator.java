package de.Ste3et_C0st.FurnitureLib.Utilitis;

public class StringTranslator {

    private String key, value;

    public StringTranslator(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return this.key;
    }

    public String getValue() {
        return this.value;
    }

}
